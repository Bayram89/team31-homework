function greet(name) {
  return "Hello, " + name + "!";
}
console.log(greet("Alice"));
console.log(greet("Mentor"));

function add(num1, num2) {
  return num1 + num2;
}
console.log(add(5, 3));
console.log(add(-4, -9));

function square(num) {
  return num * num;
}
function sumOfSquares(num1, num2) {
  return square(num1) + square(num2);
}
console.log(sumOfSquares(3, 4));
